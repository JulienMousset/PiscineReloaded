#include <stdlib.h>

int		ft_strlen(char *str)
{
	int		i;

	i = 0;
	while (str[i])
		i++;
	return (i);
}

char	*ft_strcpy(char *dest, char *src)
{
	int		i;

	i = 0;
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

char	*ft_strdup(char *src)
{
	char	*dest;

	if (!(dest = malloc(ft_strlen(src) + 1)))
		return (NULL);
	if (src == NULL)
		return (NULL);
	ft_strcpy(dest, src);
	return (dest);
}
